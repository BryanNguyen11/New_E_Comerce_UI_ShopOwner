// Hàm lấy danh sách sản phẩm có phân trang
export const getProducts = (page = 1, limit = 24) => {
  const start = (page - 1) * limit;
  const end = start + limit;
  
  // Tạo thêm sản phẩm nếu cần
  if (start >= mockProducts.length) {
    const productsNeeded = end - mockProducts.length;
    for (let i = 0; i < productsNeeded; i++) {
      const lastId = mockProducts[mockProducts.length - 1].id;
      const newId = lastId + i + 1;
      mockProducts.push({
        ...mockProducts[i % 8], // Lặp lại mẫu từ 8 sản phẩm đầu
        id: newId,
        name: `${mockProducts[i % 8].name} ${newId}`,
        sold: Math.floor(Math.random() * 15000),
        rating: (4 + Math.random()).toFixed(1),
        ratingCount: Math.floor(Math.random() * 2000) + 500,
        image: `https://picsum.photos/300/300?random=${newId}`,
        images: [
          `https://picsum.photos/800/800?random=${newId}_1`,
          `https://picsum.photos/800/800?random=${newId}_2`,
          `https://picsum.photos/800/800?random=${newId}_3`,
          `https://picsum.photos/800/800?random=${newId}_4`,
          `https://picsum.photos/800/800?random=${newId}_5`
        ]
      });
    }
  }

  return mockProducts.slice(start, end);
};

// Hàm lấy chi tiết một sản phẩm
export const getProduct = (id) => {
  return mockProducts.find(p => p.id === parseInt(id)) || null;
};

